    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
        DecimalFormat vf  = new DecimalFormat("R$ #,##0.00"); 
        Double subtotal;
  
        if(Integer.parseInt(txtQtdeVendida.getText()) == 0){
            JOptionPane.showMessageDialog(null, "A quantidade de produto tem que ser maior do que Zero!", "Quantidade incorreta.", HEIGHT);
        }else{
            
            for(int t = 0; t < produtos.size(); t++){
                if(produtos.get(t).getCodigo().equals(txtCodigoProduto.getText())){
                    if(Double.parseDouble(txtQtdeVendida.getText()) > produtos.get(t).getQtdeEstoque()){
                        JOptionPane.showMessageDialog(null, "Não há a quantidade em Estoque.\nQuantidade em estoque: " + produtos.get(t).getQtdeEstoque(), "Produto não encontrado", HEIGHT);
                        break;
                    }
                    
                    int numeroSeq=1;
                    
                    for(int p = 0; p < pedidos.size(); p++){
                        numeroSeq += pedidos.get(p).getItensPedido().size();
                    }
                    
                    ItemPedido itemPedido = new ItemPedido(numeroSeq, Integer.parseInt(txtQtdeVendida.getText()), produtos.get(t));

                    //Procurando Cliente
                    for(int c = 0; c < pessoas.size(); c++){
                        if(pessoas.get(c).getCpf().equals(txtFormatCpfCliente.getText())){
                            if(pessoas.get(c) instanceof Cliente){
                                pedido.setCliente((Cliente)pessoas.get(c));
                                break;
                            }
                        }
                    }
                    
                    //Procurando Vendedor
                    for(int c = 0; c < pessoas.size(); c++){
                        if(pessoas.get(c).getCpf().equals(txtFormatCpfCliente.getText())){
                            if(pessoas.get(c) instanceof Vendedor){
                                pedido.setVendedor((Vendedor)pessoas.get(c));
                                break;
                            }
                        }
                    }
                    
                    if(pedido.getCliente().getLimiteDisp() > itemPedido.getQtdeVendida() * produtos.get(t).getPreco()){
                        pedidos.add(pedido);
                        itemPedido.setPedido(pedidos.get(posPed));
                        pedidos.get(posPed).addItemPedido(itemPedido);
                        
                        //========= CARREGA GRID ===========               
                        String itens[] = {
                            txtCodigoProduto.getText(),
                            txtDescricaoProduto.getText(),
                            String.valueOf(vf.format(produtos.get(t).getPreco())),
                            String.valueOf(txtQtdeVendida.getText())
                        };
                        
                        qtdeTotalItens += Integer.parseInt(txtQtdeVendida.getText());
                        
                        modTbProduto.addRow(itens);
                        subtotal = itemPedido.getQtdeVendida() * produtos.get(t).getPreco();
                        modTbProduto.setValueAt(vf.format(subtotal), modTbProduto.getRowCount() - 1, 4);
                        total += subtotal;
                        txtQtdeItensPedido.setText(String.valueOf(qtdeTotalItens));
                        txtValorTotalPedido.setText(vf.format(total));
                        //==================================
                        txtCodigoProduto.setText("");
                        txtQtdeVendida.setText("");
                        txtDescricaoProduto.setText("");
                        
                        txtCodigoProduto.setEnabled(true);
                        btnConsultarProduto.setEnabled(true);
                        txtQtdeVendida.setEnabled(false);
                        btnAdicionarItem.setEnabled(false);
                        txtCodigoProduto.requestFocus();
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Não há saldo suficiente na conta.\nSaldo: " + pedidos.get(t).getCliente().getLimiteDisp(), "Saldo", HEIGHT);
                        break;
                    }
                    
                    break;
                }
            }
        }
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

