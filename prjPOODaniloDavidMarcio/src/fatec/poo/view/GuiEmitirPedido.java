    private void txtFormatDataPedidoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFormatDataPedidoFocusLost
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String dataPedidoString = txtFormatDataPedido.getText();
        
        try {
            sdf.setLenient(false);
            Date dataPedido = sdf.parse(dataPedidoString);
            
            Pedido pedido = new Pedido(txtNumeroPedido.getText(), txtFormatDataPedido.getText());
            pedidos.add(pedido);
            posPed = pedidos.size() - 1;
            txtFormatDataPedido.setEnabled(false);
            txtFormatCpfCliente.setEnabled(true);
            btnConsultarCliente.setEnabled(true);
            txtFormatCpfCliente.requestFocus();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "A data não existe, digite novamente a uma data de pedido válida!\nErro: " + ex.getMessage(), "Data Inválida", HEIGHT);
            txtFormatDataPedido.requestFocus();
        }
        
        
        
    }//GEN-LAST:event_txtFormatDataPedidoFocusLost

