    private void btnPesquisarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarPedidoActionPerformed
        DecimalFormat vf  = new DecimalFormat("R$ #,##0.00"); 
        boolean novoPedido = true;
        
        try {
            for(int t = 0; t < pedidos.size(); t++){
            if(Integer.parseInt(txtNumeroPedido.getText()) == Integer.parseInt(pedidos.get(t).getNumero())){
                double subtotal;
                double total = 0;
                posPed = t;
                
                txtFormatDataPedido.setText(pedidos.get(t).getDataEmissao());
                
                if(pedidos.get(t).getFormaPagto()){
                    cbxFormaPagamento.setSelectedIndex(0);
                }else{
                    cbxFormaPagamento.setSelectedIndex(1);
                }
                
                txtFormatCpfCliente.setText(pedidos.get(t).getCliente().getCpf());
                txtDescricaoCliente.setText(pedidos.get(t).getCliente().getNome());
                
                txtFormatCpfVendedor.setText(pedidos.get(t).getVendedor().getCpf());
                txtDescricaoVendedor.setText(pedidos.get(t).getVendedor().getNome());
                
                for(int a = 0; a < pedidos.get(t).getItensPedido().size(); a++){
                    String itens[] = {
                        pedidos.get(t).getItensPedido().get(a).getProduto().getCodigo(),
                        pedidos.get(t).getItensPedido().get(a).getProduto().getDescricao(),
                        String.valueOf(vf.format(pedidos.get(t).getItensPedido().get(a).getProduto().getPreco())),
                        String.valueOf(pedidos.get(t).getItensPedido().get(a).getQtdeVendida())
                    };
                    
                    modTbProduto.addRow(itens);
                    subtotal = pedidos.get(t).getItensPedido().get(a).getProduto().getPreco() * pedidos.get(t).getItensPedido().get(a).getQtdeVendida();
                    modTbProduto.setValueAt(vf.format(subtotal), modTbProduto.getRowCount() - 1, 4);
                    total += subtotal;
                }
                
                txtValorTotalPedido.setText(String.valueOf(vf.format(total)));
                txtQtdeItensPedido.setText(String.valueOf(pedidos.get(t).getItensPedido().size()));
                
                txtCodigoProduto.setEnabled(true);
                btnConsultarProduto.setEnabled(true);
                btnRemoverItem.setEnabled(true);
                btnAlterar.setEnabled(true);
                btnExcluir.setEnabled(true);
                
                txtNumeroPedido.setEnabled(false);
                btnPesquisarPedido.setEnabled(false);
                
                novoPedido = false;
                
                break;
            }
        }
        
        if(novoPedido){
            posPed = pedidos.size();
            pedido = new Pedido(txtNumeroPedido.getText(), txtFormatDataPedido.getText());
            txtNumeroPedido.setEnabled(false);
            btnPesquisarPedido.setEnabled(false);
            txtFormatDataPedido.setEnabled(true);
            btnConsultarCliente.setEnabled(true);
            txtFormatDataPedido.requestFocus();
        }
        
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu o erro abaixo.\n" + ex.getMessage(), "Erro de Cadastro", HEIGHT);
        }
        
        
        
        
    }//GEN-LAST:event_btnPesquisarPedidoActionPerformed

